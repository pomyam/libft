#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s) + 1;
	while (s[--i] != (char)c)
	{
		if (i == 0)
			return (0);
	}
	return ((char *)s + i);
}
